mystr = "i'm a good poor sad girl"

#checking alpha numeric or not
print(mystr.isalnum())

#checking if alpha or not
print(mystr.isalpha())

#ends with function
print(mystr.endswith("girl"))

#counting letter
print(mystr.count("a"))

#making starting letter capital
print(mystr.capitalize())

#finding strings
print(mystr.find("a"))

#lower case
print(mystr.lower())

#upper case
print(mystr.upper())

#replacing string
print(mystr.replace("a","an"))




